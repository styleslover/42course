#include "libft.h"

char	*ft_strmapi(char  *s, char (*f)(unsigned int, char))
{
	unsigned int	a;
	char			*str;

	a = 0;
	str = (char *)malloc(sizeof(char) * (ft_strlen(s)) + 1);
	if (str == NULL)
		return (NULL);
	while (s[a] != '\0')
	{
		str[a] = f(a, s[a]);
		a++;
	}
	str[a] = '\0';
	return (str);
}
